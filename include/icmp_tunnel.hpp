#pragma once

#include <cstdint>
#include <stdexcept>
#include <string>
#include <vector>

/**
 * @file icmp_tunnel.hpp
 * @brief Заголовочный файл, описывающий классы, константы и функции для ICMP туннелирования
 */

namespace tunnel {

/**
 * @brief Класс исключения, выбрасываемого при критических ошибках работы с сетью или файлами.
 *
 * Наследуется от стандартного std::runtime_error. Используется для безопасной
 * передачи информации об ошибке (например, нехватка прав root).
 */
class NetworkException : public std::runtime_error {
   public:
    /**
     * @brief Конструктор сетевого исключения.
     * @param message Текстовое сообщение с подробным описанием причины ошибки.
     */
    explicit NetworkException(const std::string& message);
};

/**
 * @brief Статический класс-утилита для работы с сырыми ICMP пакетами и системными вызовами.
 *
 * Содержит методы для инкапсуляции данных, проверки целостности (checksum)
 * и взаимодействия с операционной системой.
 */
class IcmpUtils {
   public:
    /**
     * @brief Магическая сигнатура для идентификации пакета-запроса (от клиента к серверу).
     * Значение по умолчанию: "TNRQ" (Tunnel Request).
     */
    static const std::string MAGIC_REQ;

    /**
     * @brief Магическая сигнатура для идентификации пакета-ответа (от сервера к клиенту).
     * Значение по умолчанию: "TNRS" (Tunnel Response). Позволяет клиенту игнорировать
     * автоматические ответы от ядра ОС.
     */
    static const std::string MAGIC_RES;

    /**
     * @brief Секретный ключ для XOR-шифрования полезной нагрузки.
     */
    static const std::string CRYPTO_KEY;

    /**
     * @brief Вычисляет стандартную контрольную сумму (checksum) для IP/ICMP заголовков (алгоритм
     * RFC 1071).
     * @param data Вектор байт пакета, для которого производится вычисление.
     * @return Вычисленная 16-битная контрольная сумма, готовая для вставки в заголовок (icmphdr).
     */
    static uint16_t calculate_checksum(const std::vector<uint8_t>& data);

    /**
     * @brief Применяет симметричное потоковое шифрование (XOR) к данным.
     *
     * Функция изменяет переданный вектор на месте. Так как операция XOR обратима,
     * повторный вызов этой функции с тем же ключом восстановит исходные данные.
     *
     * @param data Вектор байт для шифрования/расшифровки.
     * @param key Строка-ключ, которая будет циклически применяться к данным.
     */
    static void xor_cipher(std::vector<uint8_t>& data, const std::string& key);

    /**
     * @brief Оборачивает полезную нагрузку (команду или результат) в массив байт с добавлением
     * магической сигнатуры.
     * @param payload Данные, которые необходимо спрятать в ICMP пакете.
     * @param magic Магическая сигнатура (TNRQ или TNRS), которая будет помещена в начало полезной
     * нагрузки.
     * @return Вектор байт, готовый для копирования в секцию данных ICMP пакета.
     */
    static std::vector<uint8_t> wrap_payload(const std::string& payload, const std::string& magic);

    /**
     * @brief Проверяет размер полезной нагрузки на превышение безопасного лимита.
     * @param size Текущий размер данных в байтах.
     */
    static void validate_payload_size(size_t size);

    /**
     * @brief Проверяет наличие нужной магической сигнатуры в сырых данных и извлекает скрытый
     * текст.
     * @param raw_data Сырые данные пакета (секция данных после ICMP заголовка).
     * @param magic Ожидаемая магическая сигнатура, по которой определяется "свой" пакет.
     * @return Извлеченная строка (полезная нагрузка). Если сигнатура не совпадает или пакет слишком
     * мал, возвращается пустая строка.
     */
    static std::string extract_payload(const std::vector<uint8_t>& raw_data,
                                       const std::string& magic);

    /**
     * @brief Проверяет строку на соответствие формату IPv4-адреса.
     * @param ip Строка, содержащая предполагаемый IP-адрес.
     * @return true, если адрес валиден, иначе false.
     */
    static bool is_valid_ipv4(const std::string& ip);

    /**
     * @brief Выполняет shell-команду в операционной системе и перехватывает её стандартный вывод
     * (stdout).
     * @param command Строка с системной командой для выполнения.
     * @return Результат выполнения команды в виде строки. Если вывода нет, возвращает
     * информационное сообщение.
     */
    static std::string execute_command(const std::string& command);

    /**
     * @brief Читает содержимое файла по заданному пути (используется для передачи больших команд
     * клиентом).
     * @param filepath Абсолютный или относительный путь к файлу.
     * @return Прочитанная строка, содержащая все данные из файла.
     * @throw tunnel::NetworkException В случае, если файл не существует или недоступен для чтения.
     */
    static std::string read_from_file(const std::string& filepath);
};

}
